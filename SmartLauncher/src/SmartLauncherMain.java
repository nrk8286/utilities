    private void resetJTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int rows = model.getRowCount();
        for (int i = rows - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }
    private void showTableData() {
        try {
            List list = SmartLauncherDAO.getProgramListFromDB();
            DefaultTableModel defaultTableModel = (DefaultTableModel) jTable1.getModel();
            String imageLoc = "C:\\Users\\Inferno\\Documents\\NetBeansProjects\\SmartLauncher\\build\\classes\\resources\\launchapp.png";
            for (int i = 0; i < list.size(); i++) {
                //ImageIcon icon = new ImageIcon("resources/plane12.png");
                defaultTableModel.addRow(new Object[]{"" + (i + 1), ((Program) list.get(i)).getProgramName(), ((Program) list.get(i)).getProgramLocation(), "", imageLoc});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error Occured While Retrieving the Program List ", null, JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(SmartLauncherMain.class.getName()).log(Level.SEVERE, null, ex);
            System.exit(-1);
        }
    }

